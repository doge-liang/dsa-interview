def get_category(problem_id: int) -> str:
    """根据题目 ID 获取分类"""
    for category, ids in PROBLEM_CATEGORIES.items():
        if problem_id in ids:
            return category
    return "array"  # 默认归类到数组


def get_file_info(filepath: str) -> tuple[int, str]:
    """从文件名提取题目 ID 和 slug"""
    filename = Path(filepath).name
    # 格式: 1.two-sum.py 或 1.two-sum.123456.ac.py
    parts = filename.split(".")
    problem_id = int(parts[0])
    slug = parts[1]
    return problem_id, slug


def show_problem(problem_id: int):
    """生成题目代码并移动到正确目录"""
    # 调用 leetcode-cli 生成代码
    print(f"生成题目 {problem_id} 的代码...")
    subprocess.run(
        ["leetcode", "show", str(problem_id), "-g", "-l", "python3"], check=True
    )

    # 找到生成的文件
    import_dir = Path("leetcode-imports")
    generated_files = list(import_dir.glob(f"{problem_id}.*.py"))

    if not generated_files:
        print(f"错误：未找到题目 {problem_id} 的生成文件")
        sys.exit(1)

    generated_file = generated_files[0]
    problem_id_int, slug = get_file_info(generated_file.name)

    # 获取分类
    category = get_category(problem_id_int)

    # 目标目录
    target_dir = Path(f"src/{category}")
    target_dir.mkdir(parents=True, exist_ok=True)

    # 目标文件名（转换为下划线命名）
    target_name = f"{slug.replace('-', '_')}.py"
    target_file = target_dir / target_name

    # 移动文件
    shutil.move(str(generated_file), str(target_file))
    print(f"✓ 已移动到: {target_file}")


def sync_files():
    """同步 leetcode-imports/ 中的文件到 src/"""
    import_dir = Path("leetcode-imports")
    if not import_dir.exists():
        print("错误：leetcode-imports 目录不存在")
        sys.exit(1)

    py_files = list(import_dir.glob("*.py"))
    if not py_files:
        print("没有找到需要同步的文件")
        return

    for filepath in py_files:
        problem_id, slug = get_file_info(filepath.name)
        category = get_category(problem_id)

        # 目标目录
        target_dir = Path(f"src/{category}")
        target_dir.mkdir(parents=True, exist_ok=True)

        # 目标文件名
        target_name = f"{slug.replace('-', '_')}.py"
        target_file = target_dir / target_name

        # 移动文件
        shutil.move(str(filepath), str(target_file))
        print(f"✓ {filepath.name} → {target_file}")

    print(f"\n共同步 {len(py_files)} 个文件")


def main():
    if len(sys.argv) < 2:
        print("用法: python lc-wrapper.py <show|sync> [问题ID]")
        sys.exit(1)

    command = sys.argv[1]

    if command == "show":
        if len(sys.argv) < 3:
            print("错误：show 命令需要指定问题ID")
            sys.exit(1)
        show_problem(int(sys.argv[2]))
    elif command == "sync":
        sync_files()
    else:
        print(f"未知命令: {command}")
        sys.exit(1)


if __name__ == "__main__":
    main()
